class Condition:
    # Condition Type
    def If(self):
        print("A")
    def If_else(self):
        print("B")
    def If_else_if(self):
        print('C')
    def Nested_if(self):
        print('D')
    # Helper
    def Number_comparison(self):
        print('E')
    def Logical(self):
        print('F')
    def Condition_in_string(self):
        print('G')
    def Condition_in_list(self):
        print('H')